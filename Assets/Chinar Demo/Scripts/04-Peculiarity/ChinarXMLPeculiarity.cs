[SerializeField] //必须设置这个，要不然不能进行序列化
public class XmlData
{
    public string ID;

    [XmlAttribute] //特性：设置为属性
    public int Level;

    public float Exp;

    [XmlIgnore] //序列化中忽略掉
    public string TestIgnore = "忽略";

    //get set设置的方式也可以进行序列化，但是私有不会被生成出去
    private int _seddd = 10;
    public int Seddd
    {
        get { return _seddd; }
        set { _seddd = value; }
    }

    [XmlArray("Items"), XmlArrayItem("item")] //可以设置数组结点和子结点的名字，如果不设置则为变量名和类型名字
    public Person[] Persons;                  //支持数组

    public List<Person> PersonList; //支持泛型
}


/// <summary>
/// 人类 —— 数据
/// </summary>
public class Person
{
    [XmlAttribute] public string Name;


    public Person()
    {
    }


    public Person(string parameter)
    {
        Name = parameter;
    }
}